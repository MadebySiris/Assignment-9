public class Vehicle {
    int speed;
    int weight;

    // Constructor
    public Vehicle(int speed, int weight) {
        this.speed = speed;
        this.weight = weight;
    }

    // Setters and getters
    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getWeight() {
        return this.weight;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getSpeed() {
        return this.speed;
    }
}

